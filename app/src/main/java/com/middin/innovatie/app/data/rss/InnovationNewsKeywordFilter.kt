        Regex("\\bmedisch", RegexOption.IGNORE_CASE),
        Regex("\\bhulpmiddel", RegexOption.IGNORE_CASE),
        Regex("\\bHIPAA\\b"),
        Regex("\\bNHS\\b"),
        Regex("\\bCMS\\b"),
        Regex("\\bWHO\\b"),
        Regex("\\bNIH\\b"),
        Regex("\\bFDA\\b"),
        Regex("\\bMDR\\b"),
        Regex("\\bEU-?MDR\\b", RegexOption.IGNORE_CASE),
        Regex("\\bdiagnos", RegexOption.IGNORE_CASE),
        Regex("\\btreatment\\b", RegexOption.IGNORE_CASE),
