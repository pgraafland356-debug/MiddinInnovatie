        Regex("\\bransomware\\b", RegexOption.IGNORE_CASE),
        Regex("\\bencryption\\b", RegexOption.IGNORE_CASE),
        Regex("\\bIT\\b"),
        Regex("\\bICT\\b"),
        Regex("\\belektronic", RegexOption.IGNORE_CASE),
        Regex("\\bchip\\b", RegexOption.IGNORE_CASE),
        Regex("\\bsemiconductor\\b", RegexOption.IGNORE_CASE),
        Regex("\\b3D\\s*print", RegexOption.IGNORE_CASE),
        Regex("\\bblockchain\\b", RegexOption.IGNORE_CASE),
    )

    fun matchesTitleAndSummary(title: String, summary: String): Boolean {
        val hay = "$title $summary"
        val care = healthOrCareContext.any { it.containsMatchIn(hay) }
        val tech = innovationOrTechSignal.any { it.containsMatchIn(hay) }
        return care && tech
    }
}
