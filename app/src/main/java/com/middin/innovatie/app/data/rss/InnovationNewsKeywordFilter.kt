        Regex("\\btelemedicine\\b", RegexOption.IGNORE_CASE),
        Regex("\\btelezorg\\b", RegexOption.IGNORE_CASE),
        Regex("\\btelehealth\\b", RegexOption.IGNORE_CASE),
        Regex("\\bremote\\s+patient\\b", RegexOption.IGNORE_CASE),
        Regex("\\bremote\\s+monitor", RegexOption.IGNORE_CASE),
        Regex("\\bRPM\\b"),
