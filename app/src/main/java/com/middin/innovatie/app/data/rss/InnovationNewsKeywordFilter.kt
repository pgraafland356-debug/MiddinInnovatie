        Regex("\\bphysician", RegexOption.IGNORE_CASE),
        Regex("\\bdoctor\\b", RegexOption.IGNORE_CASE),
        Regex("\\bnurse\\b", RegexOption.IGNORE_CASE),
        Regex("\\bnursing\\b", RegexOption.IGNORE_CASE),
        Regex("\\bpharma", RegexOption.IGNORE_CASE),
        Regex("\\btherap", RegexOption.IGNORE_CASE),
        Regex("\\bbiotech\\b", RegexOption.IGNORE_CASE),
        Regex("\\bzorg\\b", RegexOption.IGNORE_CASE),
        Regex("\\bgezondheid", RegexOption.IGNORE_CASE),
        Regex("\\bziekenhuis", RegexOption.IGNORE_CASE),
        Regex("\\bverpleeg", RegexOption.IGNORE_CASE),
        Regex("\\bthuiszorg\\b", RegexOption.IGNORE_CASE),
        Regex("\\bmedicijn", RegexOption.IGNORE_CASE),
