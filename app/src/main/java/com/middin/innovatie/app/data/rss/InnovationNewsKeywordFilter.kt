        Regex("\\bEMR\\b"),
        Regex("\\binteroperab", RegexOption.IGNORE_CASE),
        Regex("\\bHL7\\b"),
        Regex("\\bFHIR\\b"),
        Regex("\\bEPD\\b", RegexOption.IGNORE_CASE),
        Regex("\\bEHD\\b", RegexOption.IGNORE_CASE),
        Regex("\\bdata\\s+exchange\\b", RegexOption.IGNORE_CASE),
        Regex("\\bbig\\s+data\\b", RegexOption.IGNORE_CASE),
        Regex("\\banalytics\\b", RegexOption.IGNORE_CASE),
